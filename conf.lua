function love.conf(t)
    t.window.title = "MiniEdit"
    t.window.width = 500
    t.window.height = 600
    t.window.borderless = false
    t.window.x = 400
    t.window.y = 100
    t.console = false
    t.window.display = 1
    t.window.vsync = true
    t.window.fullscreen = false
    t.window.fullscreentype = "desktop"
end